    private void btnEditClick() {
        model = (DefaultTableModel) tblPenjualan.getModel();
        int baris = tblPenjualan.getSelectedRow();
        int row = baris;

        if (baris >= 0) {
        
            String namaBarang = (String) cmbBarang.getSelectedItem();
            String hargaSatuan = (String) lblHarga.getText();
            String jumlahBeli = (String) txtJml.getText();
            subtotal();


            model.setValueAt(namaBarang, baris, 0);
            model.setValueAt(hargaSatuan, baris, 1);
            model.setValueAt(jumlahBeli, baris, 2);
            model.setValueAt(subTotalBelanja, baris, 3);
            
            total();
            bersihkanLayar();
            cekBarisTerpilih();
            
            tblPenjualan.clearSelection();
            barisTerpilih=false;
 
            JOptionPane.showMessageDialog(null, "Data berhasil di edit");
            
        } else {
            JOptionPane.showMessageDialog(null, "Pilih baris yang akan di-update terlebih dahulu.");
        }
       
      total();  
    }

    private void mouseClicked() {
        model = (DefaultTableModel) tblPenjualan.getModel();
       baris = tblPenjualan.getSelectedRow();
       barisTerpilih=true;
         
       txtJml.setText(String.valueOf(baris));
       String namaBarang = (String) tblPenjualan.getValueAt(baris, 0);
       String hargaSatuan = (String) tblPenjualan.getValueAt(baris, 1);
       String jumlahBeli = (String) tblPenjualan.getValueAt(baris, 2);
       String subTotal = (String) tblPenjualan.getValueAt(baris, 3);
        
       int hrgSat = Integer.parseInt(hargaSatuan);
       int jmlBeli = Integer.parseInt(jumlahBeli);
       int sub2=hrgSat*jmlBeli;
       
       int row = baris;
       
       model.setValueAt(namaBarang, row, 0);
       model.setValueAt(hargaSatuan, row, 1);
       model.setValueAt(jumlahBeli, row, 2);
       
       cmbBarang.setSelectedItem(namaBarang.toString());
       txtJml.setText(jumlahBeli);
       
        if(namaBarang.equals("Es Krim")){
            cmbBarang.setSelectedIndex(1);
       }else if(namaBarang.equals("Chitato")){
            cmbBarang.setSelectedIndex(2);
        }else if(namaBarang.equals("Sikat Gigi")){
            cmbBarang.setSelectedIndex(3);
        }else if(namaBarang.equals("Pepsodent")){
            cmbBarang.setSelectedIndex(4);
        }else{
           cmbBarang.setSelectedIndex(0);
       }
        
        lblHarga.setText(hargaSatuan);
        txtJml.setText(jumlahBeli);
        lblTotal.setText(subTotal);
    }

    private int subtotal() {
        jmlBeli = Integer.valueOf(txtJml.getText());
        hrgSatuan = Integer.valueOf(lblHarga.getText());
        subTotalBelanja=jmlBeli*hrgSatuan;
        return subTotalBelanja;
    }

    private void bersihkanLayar() {
        txtJml.setText("");
       lblHarga.setText("0");
       cmbBarang.setSelectedIndex(0);
    }

    private void total() {
        model = (DefaultTableModel) tblPenjualan.getModel();
        int jumlah = model.getRowCount();
        int sumTotal = 0;
        
        for (int i = 0; i < jumlah; i++) {
            int dataTotal = Integer.valueOf(model.getValueAt(i, 3).toString()); //i, 3 itu adalah kolom ke berapa yg mau ditampilkan jumlahnya. jadi i, 3 adalah kolom subtotal karna index dihitung dari 0.
            sumTotal += dataTotal;
        }
        lblTotal.setText(Integer.toString(sumTotal));     
    }

    private void keyDelete() {
        barisTerpilih=false;
        cmbBarang.setSelectedIndex(0);
        lblHarga.setText("0");
        txtJml.setText("");
        lblTotal.setText("0");
    }

    private boolean cekBarisTerpilih() {
        return true;
    }

