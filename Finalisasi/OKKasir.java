    private void txtJmlKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJmlKeyPressed
        // TODO add your handling code here:
        model = (DefaultTableModel) tblPenjualan.getModel();
            List list = new ArrayList<>(); //List adalah class dan list adalah variabel
            tblPenjualan.setAutoCreateColumnsFromModel(true);
            list.add(cmbBarang.getSelectedItem().toString());
            list.add(lblHarga.getText()); 
            list.add(txtJml.getText());
            
            
            if(evt.getKeyCode()== KeyEvent.VK_ENTER){ //tekan enter
                if(barisTerpilih){
                    btnEditClick();
                }else{
                    subtotal();
                    list.add(String.valueOf(subTotalBelanja));
                    model.addRow(list.toArray());  
                    bersihkanLayar();
                    total();
                }
            }
            
            if(evt.getKeyCode()== KeyEvent.VK_DELETE){
                if(barisTerpilih){
                    keyDelete();
                    int selectedRow = tblPenjualan.getSelectedRow();
                    model = (DefaultTableModel) tblPenjualan.getModel();
                    model.removeRow(selectedRow);
                    JOptionPane.showMessageDialog(null, "Data berhasil di hapus");
                }
            }
            
            
    }//GEN-LAST:event_txtJmlKeyPressed

